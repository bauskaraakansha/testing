//Program for implicit casting
package TypeCasting;

public class ImplicitCasting {

	public static void main(String[] args) 
	{
		int x='a';
		double d= 10;
		char c=65;
		float f=20;
		char a=64;
		
		System.out.println("conversion of character to int= "+x);
		System.out.println("conversion of int to double= "+d);
		System.out.println("conversion of int to char= "+c);
		System.out.println("conversion of int to float= "+f);
		System.out.println("conversion of int to char= "+a);
		
		
		
	}

}
